from products p
join supplier s
on p.sup_id=s.sup_id
group by p.sup_id;

-- ● Which suppliers contribute the most to total product sales (by revenue)? 

select p.sup_id,s.sup_name,sum(o.total_price) total_price
from products p
join supplier s
on p.sup_id=s.sup_id
join order_details o
on p.prod_id=o.prod_id
group by p.sup_id
order by total_price desc;


-- 5 . Employee Performance 
-- Assess how employees are handling and influencing sales. 
-- ● How many employees have processed orders? 

select count(distinct emp_id) from orders;

-- ● Which employees have handled the most orders? 

select e.emp_name,count(*) orders 
from orders o
join employees e
on e.emp_id=o.emp_id
group by o.emp_id
order by orders desc;

-- ● What is the total sales value processed by each employee? 

select o.emp_id,sum(d.total_price) total_sales
from order_details d
join orders o
on o.ord_id=d.ord_id
group by o.emp_id;

-- ● What is the average order value handled per employee? 

select o.emp_id, avg(t.order_value) avg_order_value
from (select ord_id,sum(total_price) order_value from order_details
group by ord_id) as t
join orders o
ON t.ord_id = o.ord_id
group by o.emp_id;

-- 6 . Order Details Deep Dive 
-- Explore item-level sales patterns and pricing behavior.  
-- ● What is the relationship between quantity ordered and total price? 

SELECT quantity, avg(total_price)
FROM order_details
group by quantity
order by quantity;

-- ● What is the average quantity ordered per product? 

SELECT p.prod_id,p.prod_name, avg(o.quantity) avg_quantity
FROM order_details o
join products p
on p.prod_id=o.prod_id
group by prod_id
order by prod_id;

-- ● How does the unit price vary across products and orders?
SELECT
    p.prod_id,
    p.prod_name,
    AVG(od.total_price / od.quantity) AS avg_unit_price
FROM order_details od
JOIN products p
ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name
ORDER BY avg_unit_price ;