-- Analysis Questions 
-- 1 . Customer Insights 
-- Gain an understanding of customer engagement and purchasing behavior. 
-- ● How many unique customers have placed orders? 

select count(distinct cust_id) customers
from orders;

-- ● Which customers have placed the highest number of orders? 

select cust_id,count(ord_id) total_orders
from orders
group by cust_id
order by total_orders desc
limit 5;


-- ● What is the total and average purchase value per customer? 

select cust_id,sum(order_value) total_purchase, avg(order_value) avg_purchase
from (
select o.cust_id,o.ord_id,sum(d.total_price) order_value
from orders o
join order_details d
on o.ord_id=d.ord_id
group by o.cust_id,o.ord_id) as t
group by cust_id;

-- ● Who are the top 5 customers by total purchase amount? 

select o.cust_id,sum(d.total_price) total_purchase
from orders o
join order_details d
on o.ord_id=d.ord_id
group by o.cust_id
order by total_purchase desc
limit 5;

-- 2. Product Performance 
-- Evaluate how well products are performing in terms of sales and revenue. 
-- ● How many products exist in each category? 

select c.cat_name,
       count(*) as no_of_products
from products p
join categories c
on p.cat_id=c.cat_id
group by c.cat_name;

-- ● What is the average price of products by category? 

select c.cat_name,avg(p.price) avg_prod_price
from products p
join categories c
on c.cat_id=p.cat_id
group by cat_name;

-- ● Which products have the highest total sales volume (by quantity)? 

select p.prod_name, sum(o.quantity) total_quantity_sales
from products p
join order_details o
on o.prod_id=p.prod_id
group by prod_name
order by total_quantity_sales desc
limit 5;

-- ● What is the total revenue generated by each product? 

SELECT p.prod_id,
       p.prod_name,
       SUM(od.total_price) AS total_revenue
FROM products p
JOIN order_details od
ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name;

-- ● How do product sales vary by category and supplier? 

select c.cat_name,sup_name,sum(o.total_price) total_sales
from products p
join order_details o 
on o.prod_id=p.prod_id
join categories c
on c.cat_id=p.cat_id
join supplier s
on s.sup_id=p.sup_id
group by c.cat_name,sup_name;

-- 3. Sales and Order Trends 
-- Analyze business performance through orders and revenue over time. 
-- ● How many orders have been placed in total? 

select count(*)total_orders from orders;

-- ● What is the average value per order? 

select avg(order_value) avg_order_value
from (
select ord_id, sum(total_price) order_value
from order_details
group by ord_id) as t;

-- ● On which dates were the most orders placed? 

select order_date,count(*) orders_placed from orders
group by order_date
order by orders_placed desc
limit 2;


-- ● What are the monthly trends in order volume and revenue? 

SELECT MONTH(STR_TO_DATE(o.order_date,'%m/%d/%y')) AS month_no,
       COUNT(DISTINCT o.ord_id) AS total_orders,
       SUM(od.total_price) AS revenue
FROM orders o
JOIN order_details od
ON o.ord_id = od.ord_id
GROUP BY month_no
ORDER BY month_no;

-- ● How do order patterns vary across weekdays and weekends? 

select case 
when weekday(str_to_date(order_date,'%m/%d/%y')) in (5,6) then 'Weekend'
    else 'Weekday'
end as type_of_week,
count(distinct o.ord_id) total_orders ,sum(d.total_price) total_price
from orders o
join order_details d
on o.ord_id = d.ord_id
group by type_of_week;

-- 4 . Supplier Contribution 
